class CountryDetailsModel {
  CountryDetailsModel({
    this.countryName,
    this.alpha2Code,
    this.countryCode,
    this.currency,
    this.states,
  });

  String? countryName;
  String? alpha2Code;
  String? countryCode;
  String? currency;
  List<StateModel>? states;

  factory CountryDetailsModel.fromJson(Map<dynamic, dynamic> json) =>
      CountryDetailsModel(
        countryName: json["country_name"],
        alpha2Code: json["alpha2_code"],
        countryCode: json["country_code"],
        currency: json["currency"],
        states: List<StateModel>.from(
            json["states"].map((x) => StateModel.fromJson(x))),
      );

  Map<String, dynamic> toJson() => {
        "country_name": countryName,
        "alpha2_code": alpha2Code,
        "country_code": countryCode,
        "currency": currency,
        "states": List<dynamic>.from(states!.map((x) => x.toJson())),
      };
}

class StateModel {
  StateModel({
    this.stateName,
  });

  String? stateName;

  factory StateModel.fromJson(Map<String, String> json) => StateModel(
        stateName: json["state_name"],
      );

  Map<String, String> toJson() => {
        "state_name": stateName!,
      };
}

